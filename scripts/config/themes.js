const FALLBACK_THEME = "theme001";

const THEME_NAME_ADJECTIVES = [
  "Solar",
  "Lunar",
  "Neon",
  "Velvet",
  "Crimson",
  "Azure",
  "Golden",
  "Silver",
  "Emerald",
  "Amber",
];

const THEME_NAME_NOUNS = [
  "Pulse",
  "Nova",
  "Horizon",
  "Echo",
  "Bloom",
  "Drift",
  "Flare",
  "Mist",
  "Forge",
  "Tide",
];

export const COLOR_SECTIONS = Object.freeze({
  surfaces: Object.freeze({
    background: "--theme-c1",
    layer: "--theme-c2",
  }),
  accents: Object.freeze({
    primary: "--theme-c3",
    secondary: "--theme-c4",
  }),
  text: Object.freeze({
    primary: "--theme-c5",
  }),
});

function normalizeHexColor(value, fallback = "#000000") {
  const raw = String(value || "").trim();
  const match = raw.match(/^#([0-9a-fA-F]{3}|[0-9a-fA-F]{6})$/);
  if (!match) return fallback;
  const hex = match[1];
  if (hex.length === 3) {
    return `#${hex
      .split("")
      .map((ch) => `${ch}${ch}`)
      .join("")
      .toUpperCase()}`;
  }
  return `#${hex.toUpperCase()}`;
}

function normalizePalette(colors) {
  if (!Array.isArray(colors) || colors.length < 5) return null;
  return colors.slice(0, 5).map((color) => normalizeHexColor(color));
}

function paletteToSections(palette) {
  const [background, layer, primary, secondary, textPrimary] = palette;
  return {
    surfaces: { background, layer },
    accents: { primary, secondary },
    text: { primary: textPrimary },
  };
}

function getThemeNumber(themeKey) {
  const n = Number(String(themeKey || "").replace("theme", ""));
  return Number.isFinite(n) ? n : 0;
}

function toPaddedThemeNumber(themeKey) {
  return String(getThemeNumber(themeKey)).padStart(3, "0");
}

function getThemeSection(themeKey) {
  const n = getThemeNumber(themeKey);
  if (n <= 20) return "Neon & Dark";
  if (n <= 40) return "Pastel & Pop";
  if (n <= 60) return "Earth & Ocean";
  if (n <= 80) return "Retro & Synth";
  return "Experimental Mix";
}

function getGeneratedThemeName(themeKey) {
  const n = getThemeNumber(themeKey);
  const adjective = THEME_NAME_ADJECTIVES[(Math.max(n, 1) - 1) % THEME_NAME_ADJECTIVES.length];
  const noun = THEME_NAME_NOUNS[(Math.max(n, 1) - 1) % THEME_NAME_NOUNS.length];
  return `${adjective} ${noun} ${toPaddedThemeNumber(themeKey)}`;
}

function normalizeThemes() {
  for (const [themeKey, themeValue] of Object.entries(THEMES)) {
    if (!themeValue || !Array.isArray(themeValue.colors)) continue;
    const normalizedPalette = normalizePalette(themeValue.colors);
    if (!normalizedPalette) continue;
    themeValue.colors = normalizedPalette;
    themeValue.sections = paletteToSections(normalizedPalette);
    if (!themeValue.name) themeValue.name = getGeneratedThemeName(themeKey);
    if (!themeValue.section) themeValue.section = getThemeSection(themeKey);
  }
}

normalizeThemes();

export function getThemeMeta(themeName) {
  const key = THEMES[themeName] ? themeName : FALLBACK_THEME;
  const theme = THEMES[key] || THEMES[FALLBACK_THEME];
  return {
    key,
    name: theme?.name || getGeneratedThemeName(key),
    section: theme?.section || getThemeSection(key),
  };
}

export function getThemePalette(themeName) {
  const key = THEMES[themeName] ? themeName : FALLBACK_THEME;
  const palette = THEMES[key]?.colors || THEMES[FALLBACK_THEME].colors;
  return [...palette];
}

export function getThemeColorSections(themeName) {
  const key = THEMES[themeName] ? themeName : FALLBACK_THEME;
  const sections = THEMES[key]?.sections;
  if (sections) {
    return {
      surfaces: { ...sections.surfaces },
      accents: { ...sections.accents },
      text: { ...sections.text },
    };
  }
  const palette = getThemePalette(key);
  return paletteToSections(palette);
}

export function buildThemeTokens(themeName) {
  const sections = getThemeColorSections(themeName);
  return {
    [COLOR_SECTIONS.surfaces.background]: sections.surfaces.background,
    [COLOR_SECTIONS.surfaces.layer]: sections.surfaces.layer,
    [COLOR_SECTIONS.accents.primary]: sections.accents.primary,
    [COLOR_SECTIONS.accents.secondary]: sections.accents.secondary,
    [COLOR_SECTIONS.text.primary]: sections.text.primary,
  };
}
